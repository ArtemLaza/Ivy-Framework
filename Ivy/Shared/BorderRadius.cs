namespace Ivy.Shared;

/// <summary>
/// Specifies border radius options for controlling corner rounding in UI elements.
/// </summary>
public enum BorderRadius
{
    /// <summary>No border radius - sharp rectangular corners.</summary>
    None,

    /// <summary>Standard rounded corners with moderate curvature.</summary>
    Rounded,

    /// <summary>Fully rounded corners creating pill or circular shapes.</summary>
    Full
}