namespace Ivy.Shared;

/// <summary>
/// Specifies alignment options for positioning content within UI elements across the Ivy framework.
/// </summary>
public enum Align
{
    /// <summary>Aligns content to the top-left corner.</summary>
    TopLeft,

    /// <summary>Aligns content to the top-right corner.</summary>
    TopRight,

    /// <summary>Aligns content to the top-center position.</summary>
    TopCenter,

    /// <summary>Aligns content to the bottom-left corner.</summary>
    BottomLeft,

    /// <summary>Aligns content to the bottom-right corner.</summary>
    BottomRight,

    /// <summary>Aligns content to the bottom-center position.</summary>
    BottomCenter,

    /// <summary>Aligns content to the left side.</summary>
    Left,

    /// <summary>Aligns content to the right side.</summary>
    Right,

    /// <summary>Centers content both horizontally and vertically.</summary>
    Center,

    /// <summary>Stretches content to fill available space.</summary>
    Stretch
}