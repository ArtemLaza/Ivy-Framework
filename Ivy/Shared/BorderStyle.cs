namespace Ivy.Shared;

/// <summary>
/// Specifies border style options for controlling the visual appearance of border lines.
/// </summary>
public enum BorderStyle
{
    /// <summary>No border - completely invisible border.</summary>
    None,

    /// <summary>Dotted border - series of round dots.</summary>
    Dotted,

    /// <summary>Dashed border - series of short line segments.</summary>
    Dashed,

    /// <summary>Solid border - continuous unbroken line.</summary>
    Solid
}